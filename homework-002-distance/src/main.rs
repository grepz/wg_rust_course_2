fn radians(p: &(f64, f64)) -> (f64, f64) {
    (p.0.to_radians(), p.1.to_radians())
}

fn haversine_distance(p1: &(f64, f64), p2: &(f64, f64)) -> f64 {
    let haversine_fn = |theta: f64| (1.0 - theta.cos()) / 2.0;

    let point1 = radians(p1); //(p1.0.to_radians(), p1.1.to_radians());
    let point2 = radians(p2); //(p2.0.to_radians(), p2.1.to_radians());

    let hav_delta_phi = haversine_fn(point2.0 - point1.0);
    let hav_delta_lambda =
        point1.0.cos() * point2.0.cos() * haversine_fn(point2.1 - point1.1);
    let total_delta = hav_delta_phi + hav_delta_lambda;

    (2.0 * 6371e3 * total_delta.sqrt().asin() * 1000.0).round() / 1000.0
}


// Function should calculate the traveled distance between two points (start and end chosen by timestamp)
// [1, 2, 3, 4, 5] - start = 2, end = 4 => [2, 3, 4] => distance(2, 3) + distance(3, 4)
// Telemetry data is represented as slice of (latitude, longitude, timestamp) and sorted by timestamp
// The formula for distance calculation is not important for this task
fn calculate_distance(points: &[(f64, f64, u64)], start: u64, end: u64) -> f64 {
    let x: f64 = 0.0;

    let path = points
        .iter()
        .filter(|(_lat, _lon, ts)| *ts >= start && *ts <= end)
        .fold(
            (0.0, (-1.0, -1.0)),
            |(dist, last_rec), (lat, lon, _ts)| -> (f64, (f64, f64)) {
              if last_rec == (-1.0, -1.0) {
                  (0.0, (*lat, *lon))
              } else {
                  (dist + haversine_distance(&last_rec, &(*lat, *lon)), (*lat, *lon))
              }
            }
        );

    path.0
}
